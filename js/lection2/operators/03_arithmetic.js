/**
 * Остаток от деления (%)
 * Инкремент (++) "префиксный инкремент" ++х и "постфиксный инкремент" х++
 * Декремент (--)
 * Унарный минус (-)
 * Унарный плюс (+)
 * Возведение в степень (**) 2 ** 3 вернет 8
 */

let a = 0;

a = ++a; // 0 + 1 => a = 1
a = a++; // a = a => a + 1

console.log(a);

let b = 2 ** 3 // 2 - основание 3 - степень

console.log(b);


